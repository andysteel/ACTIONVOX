package com.gmail.andersoninfonet.actionvox.modelo;

public class Passageiro {
	private long cpf;
	private String nome;
	private String email;
	private String telpessoal;
	private String telcontato;
	private long localizador;
	
	
	public long getCpf() {
		return cpf;
	}
	public void setCpf(long cpf) {
		this.cpf = cpf;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getTelpessoal() {
		return telpessoal;
	}
	public void setTelpessoal(String telpessoal) {
		this.telpessoal = telpessoal;
	}
	public String getTelcontato() {
		return telcontato;
	}
	public void setTelcontato(String telcontato) {
		this.telcontato = telcontato;
	}
	public long getLocalizador() {
		return localizador;
	}
	public void setLocalizador(long localizador) {
		this.localizador = localizador;
	}
	
	

}
